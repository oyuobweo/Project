/**
 * 특정 날짜의 공휴일 이름을 반환합니다.
 * @param {Date} date 
 * @returns {string|null} 공휴일 이름 또는 null
 */
export const getHoliday = (date) => {
  if (!date) return null;
  const key = format(date, 'yyyy-MM-dd');
  return HOLIDAYS[key] || null;
};

/**
 * 일요일 또는 공휴일 여부를 확인합니다.
 * @param {Date} date 
 * @returns {boolean}
 */
export const isRedDay = (date) => {
  if (!date) return false;
  // 일요일(0) 이거나 공휴일에 등록된 경우
  return date.getDay() === 0 || !!getHoliday(date);
};
